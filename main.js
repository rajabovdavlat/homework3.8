// 1-masala:Foydalanuvchi ob-havo gradusini kiritsin.
// Agar gradus -20 dan past bo'lsa "Qahraton"
// Agar gradus 0 va -20 oralig'ida bo'lsa "Juda sovuq"
// Agar gradus 0 va 10 oralig'ida bo'lsa "Sovuq havo"
// Agar gradus 10 va 20 oralig'ida bo'lsa "Salqin havo"
// Agar gradus 20 va 30 oralig'ida bo'lsa "Iliq havo"
// Agar gradus 30 va 40 oralig'ida bo'lsa "Issiq havo"
// Agar gradus 40 dan baland bo'lsa "Jazirama" alertda chiqsin

const harorat = parseInt(prompt("gradusni kiriting"));

if (harorat < -20) {
  console.log("Qahraton");
} else if (harorat >= -20 && harorat < 0) {
  console.log(`Juda sovuq`);
} else if (harorat >= 0 && harorat < 10) {
  console.log("Sovuq havo");
} else if (harorat >= 10 && harorat < 20) {
  console.log("Salqin havo");
} else if (harorat >= 20 && harorat < 30) {
  console.log("Iliq havo");
} else if (harorat >= 30 && harorat < 40) {
  console.log("Isiq havo");
} else if (harorat >= 40) {
  alert("Jazirama");
}

